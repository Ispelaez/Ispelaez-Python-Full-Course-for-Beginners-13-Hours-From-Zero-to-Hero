# Boolean Values & Comparison Operators

a = 10
b = 5

print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)
print("a != b:", a != b)
print("a >= 10:", a >= 10)
print("b <= 5:", b <= 5)

# Boolean conversion
print("\nBoolean conversion:")
print(bool(1))     # True
print(bool(0))     # False
print(bool(""))    # False
print(bool("Hi"))  # True