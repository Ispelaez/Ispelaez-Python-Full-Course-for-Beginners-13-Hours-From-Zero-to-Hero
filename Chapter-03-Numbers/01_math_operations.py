# Math Operations & Types

a = 10
b = 3

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("Division:", a / b)        # float
print("Division entera:", a // b)
print("Modulo:", a % b)
print("Potencia:", a ** b)

# Tipos de datos
print("\nTipos:")
print(type(a))
print(type(a / b))  # siempre float

# Jerarquia de operaciones
resultado = 2 + 3 * 4
print("\nResultado (2 + 3 * 4):", resultado)